#####################将函数存储在模块中#################
#方法1：导入整个模块                      import module_name
#      调用                                module_name.function_name()
#方法2：导入模块中特定的一个或多个函数：     from module_name import function_name1, function_name2, ……
#      调用                                function_name()
#方法3：导入模块中特定的函数并给函数指定别名：from module_name import function_name as fn
#      调用                                fn()
#方法4：导入整个模块并给模块重命名：         import module_name as mn
#      调用                                mn.function_name()
#方法5：导入模块中的所有函数：              from module_name import *
#      调用                                function_name()
